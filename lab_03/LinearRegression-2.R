# ------------------------------------------------------------------
# BU???C 6: PHÂN TÍCH H??? S??? H???I QUY
# ------------------------------------------------------------------

# coef(model): vector h??? s??? g???m Intercept + 13 bi???n
# [-1]: b??? Intercept, ch??? l???y h??? s??? c???a các bi???n d???c l???p
coef_df <- data.frame(
  feature     = names(coef(model)[-1]),   # Tên bi???n
  coefficient = coef(model)[-1]           # Giá tr??? h??? s??? tuong ???ng
)

# S???p x???p gi???m d???n theo giá tr??? h??? s???
# H??? s??? duong l???n ??? bi???n tác d???ng m???nh chi???u tang giá nhà
# H??? s??? âm l???n   ??? bi???n tác d???ng m???nh chi???u gi???m giá nhà
