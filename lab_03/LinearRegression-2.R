train_indices <- createDataPartition(y, p = 0.8, list = FALSE)

# Chia X và y theo ch??? s??? train
X_train <- X[train_indices, ]    # Ma tr???n d???c trung t???p hu???n luy???n
X_test  <- X[-train_indices, ]   # Ma tr???n d???c trung t???p ki???m tra (d???u '-' = lo???i tr???)
y_train <- y[train_indices]      # Nhăn m???c tiêu t???p hu???n luy???n
