# predict() áp d???ng mô h́nh dă hu???n luy???n lên d??? li???u m???i (X_test)
# newdata ph???i có dúng tên c???t nhu d??? li???u train
y_pred <- predict(model, newdata = X_test)

# ------------------------------------------------------------------
# BU???C 8: DÁNH GIÁ MÔ H̀NH B???NG CÁC CH??? S??? H???I QUY
# ------------------------------------------------------------------

# MSE (Mean Squared Error): trung b́nh b́nh phuong sai s???
# Nh???y c???m v???i outlier; don v??? là $1000² ??? khó di???n gi???i tr???c ti???p
mse <- mean((y_test - y_pred)^2)
mse

# RMSE (Root MSE): can b???c hai c???a MSE
