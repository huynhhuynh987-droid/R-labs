# Công th???c 'y_train ~ .' nghia là:
#   y_train là bi???n ph??? thu???c
#   d???u '.' = s??? d???ng T???T C??? các c???t c̣n l???i trong data = X_train làm bi???n d???c l???p
# ??? Mô h́nh: medv = ??0 + ??1*crim + ??2*zn + ... + ??13*lstat + ??
model <- lm(y_train ~ ., data = X_train)

# K???t qu??? summary g???m:
#   - Coefficients: h??? s???, Std. Error, t-value, p-value t???ng bi???n
#   - Multiple R²: m???c d??? gi???i thích phuong sai c???a mô h́nh trên t???p train
#   - F-statistic: ki???m d???nh toàn b??? mô h́nh có ư nghia không
